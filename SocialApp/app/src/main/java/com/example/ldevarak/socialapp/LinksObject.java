public class LinksObject {

    int id;
    String linkname;
    String url;
    String category;
    String favourite;

    public LinksObject(){
    }

    public int getId() {
        return id;
    }

    public String getLinkname() {
        return linkname;
    }

    public String getUrl() {
        return url;
    }

    public String getCategory() {
        return category;
    }

    public String getFavourite() {
        return favourite;
    }
}
